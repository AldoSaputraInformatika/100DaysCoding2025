package day35;

import java.util.Scanner;

public class Day35 {

    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int angka = input.nextInt();
        if (angka == 1) {
            System.out.println("Keren");
        } else if (angka == 2) {
            System.out.println("Mantap");
        } else if (angka == 3) {
            System.out.println("Hebat");
        } else {
            System.out.println("Mantap");
        }
    }
}
