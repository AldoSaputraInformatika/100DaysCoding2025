package deklarasiinisialisaliupdatevariabel;
public class DeklarasiInisialisaliUpdateVariabel {
    public static void main(String[] args) {
 
      int umur = 50;
      /* Saya memdeklarasikan suatu variabel bernama umur 
      bertipe integer dan menginisialisasikan atau memberi 
      nilai awal variabel tersebut 50*/
     
     umur += 1;
     umur += 3;
     umur *= 3;
     /* saya mengupdate/memperbarui nilai variabelnya sebanyak 3x 
     dengan nilai awal 50 ke 51 ke 54 ke 162 dimana 162
     adalah nilai akhir dari variabel umur
     */
     
     System.out.println(umur);

    }
    
}
