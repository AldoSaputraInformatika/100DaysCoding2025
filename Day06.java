public class Tipedata{
    public static void main(String[]args){
        //Tipe data pecahan
       
        double A = 90.5;
        // ukuran 64 bit (8 byte)
       
        float B = (float) 90.8;
        float C = 90.73f;
        // ukuran 32 biit (4 byte)
        System.out.println(A);
        System.out.println(B);
        System.out.println(C);
    }
}
