
import java.util.Scanner;

public class Day66dua {

    public static void main(String[] args) {
        Scanner input = new Scanner(System.in);
        int n = input.nextInt();
        for (int i = 1; i <= n; i++) {
            System.out.println("H");
            System.out.println("A");
            System.out.println("L");
            System.out.println("O");
            System.out.println();
        }
    }

}
